    private void tempoTela() {
        tempo = controle.getTempoRodada();
        barraTempo.setMaximum(tempo);
        barraTempo.setValue(tempo);

        timer = new Timer();
        task = new TimerTask() {
            @Override
            public void run() {
                tempo--;

                barraTempo.setValue(tempo);

                int minutos = tempo / 60;
                int segundos = tempo % 60;

                if (minutos == 0 && segundos <= 30) {
                    barraTempo.setForeground(Color.RED);
                } else {
                    Color color = new Color(51, 153, 255);
                    barraTempo.setForeground(color);
                }

                if (tempo > 0) {
                    if (segundos < 10) {
                        barraTempo.setString(minutos + ":0" + segundos);
                    } else {
                        barraTempo.setString(minutos + ":" + segundos);
                    }
                } else {
                    finalizaJogo();
                }
            }
        };
    }

    private void iniciarTempo() {
        timer.schedule(task, 0, 1000);
    }

    private void pararTempo() {
        timer.cancel();
    }

