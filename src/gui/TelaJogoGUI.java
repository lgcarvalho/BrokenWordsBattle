    private void botaoHabilidadeHeroiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoHabilidadeHeroiActionPerformed
        botaoHabilidadeHeroi.setEnabled(false);

        if (heroiSelecionado.equals("CIBORG")) {
            int pontuacaoAtual = Integer.parseInt(campoPontuacao.getText());

            pontuacaoAtual *= 2;

            campoPontuacao.setText(Integer.toString(pontuacaoAtual));
        } else if (heroiSelecionado.equals("ANDROID")) {
            int vidaJogador = barraJogador.getMaximum();

            barraJogador.setValue(vidaJogador);
        } else if (heroiSelecionado.equals("PYRO")) {
            int pontuacaoAtual = Integer.parseInt(campoPontuacao.getText());

            pontuacaoAtual += 3000;

            campoPontuacao.setText(Integer.toString(pontuacaoAtual));

            reiniciarNivel();
        } else if (heroiSelecionado.equals("VIKING")) {
            tempo *= 2;
        }

        controle.reiniciarHabilidade();
        barraHabilidade.setValue(0);
    }//GEN-LAST:event_botaoHabilidadeHeroiActionPerformed

