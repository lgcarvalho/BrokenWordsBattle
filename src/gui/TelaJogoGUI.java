    private void validarJogada() {
        if (controle.verificarJogada() == true) {
            if (palavras.verificarPalavra() == true) {
                campoPalavra.setForeground(new Color(51, 102, 0));

                controle.reiniciaBotao();

                tempo += 6;

                int vidaMestre = barraMestre.getValue();
                vidaMestre -= 10;
                barraMestre.setValue(vidaMestre);

                controle.somaPontuacao();

                campoPontuacao.setText(Integer.toString(controle.getPontuacao()));

                if (!heroiSelecionado.equals("APRENDIZ")) {
                    controle.somaHabilidade();
                    barraHabilidade.setValue(controle.getHabilidade());

                    if (controle.verificaHabilidade() == true) {
                        botaoHabilidadeHeroi.setEnabled(true);
                    }
                }

                if (barraMestre.getValue() == 0) {
                    reiniciarNivel();

                    controle.setMultiplicadorNivel();
                    controle.setPontosGanhos();
                }
            } else {
                campoPalavra.setForeground(new Color(153, 0, 0));

                reiniciarBotoes();

                int vidaJogador = barraJogador.getValue();
                vidaJogador -= 5;
                barraJogador.setValue(vidaJogador);

                if (vidaJogador == 0) {
                    finalizaJogo();
                }

                controle.reiniciaBotao();
            }
        } else {
            campoPalavra.setForeground(new Color(0, 0, 0));
        }
    }

    public void finalizaJogo() {
        pararTempo();

        if (apelido.equals("Convidado")) {
            TelaPrincipalGUI telaPrincipal = new TelaPrincipalGUI();

            telaPrincipal.setVisible(true);

            this.dispose();
        } else {
            TelaFinalizaGUI telaFinaliza = new TelaFinalizaGUI();

            telaFinaliza.usuario = usuario;
            telaFinaliza.apelido = apelido;
            telaFinaliza.idJogador = idJogador;
            telaFinaliza.textoApelido.setText(textoJogador.getText());
            telaFinaliza.imagemJogador.setIcon(botaoHabilidadeHeroi.getIcon());
            telaFinaliza.textoPontuacaoTotal.setText(campoPontuacao.getText());

            telaFinaliza.iniciarTela();

            telaFinaliza.setVisible(true);

            this.dispose();
        }

    }

    public void iniciarMestre() {
        MestreDAO mestreDao = new MestreDAO();

        lista = mestreDao.pesquisaMestre();
    }

    public void escolheMestre() {
        if (!lista.isEmpty()) {
            Random r = new Random();
            int indice = r.nextInt(lista.size());

            Mestre mestre = lista.remove(indice);

            textoMestre.setText(mestre.getNome());
            imagemMestre.setIcon(new javax.swing.ImageIcon(getClass().getResource(mestre.getImagem())));
        } else {
            iniciarMestre();
            escolheMestre();
        }
    }

