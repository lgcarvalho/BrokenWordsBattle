    public void populaTela(Login login) {
        JogadorDAO jogadorDao = new JogadorDAO();

        jogador = jogadorDao.pesquisaJogador(login.getUsuario());

        Heroi heroi = new Heroi(jogador.getHeroiSelecionado());

        heroi.defineHeroi();

        String imagem = heroi.getImagemHeroi();
        Color color = heroi.getColor();

        botaoHerois.setIcon(new javax.swing.ImageIcon(getClass().getResource(imagem)));
        botaoHerois.setBackground(color);

        textoHeroiSelecionado.setText(jogador.getHeroiSelecionado());
        textoUsuario.setText(login.getUsuario());
        textoApelido.setText(login.getApelido());
        textoPontuacao.setText(Integer.toString(jogador.getPontuacao()));
        textoMoeda.setText(Integer.toString(jogador.getCarteira()));
        textoAgua.setText(Integer.toString(jogador.getAguaMineral()));
        textoGarrafa.setText(Integer.toString(jogador.getGarrafaCafe()));
        textoVitamina.setText(Integer.toString(jogador.getVitamina()));
    }

