    private void botaoJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoJogarActionPerformed
        TelaJogoGUI telaJogo = new TelaJogoGUI();

        telaJogo.botaoHabilidadeHeroi.setBackground(botaoHerois.getBackground());
        telaJogo.botaoHabilidadeHeroi.setIcon(botaoHerois.getIcon());
        telaJogo.textoJogador.setText(textoApelido.getText());

        int qtdAgua = Integer.parseInt(textoAgua.getText());
        int qtdGarrafa = Integer.parseInt(textoGarrafa.getText());
        int qtdVitamina = Integer.parseInt(textoVitamina.getText());

        if (qtdAgua > 0) {
            telaJogo.botaoAgua.setEnabled(true);
        } else {
            telaJogo.botaoAgua.setEnabled(false);
        }

        if (qtdGarrafa > 0) {
            telaJogo.botaoGarrafa.setEnabled(true);
        } else {
            telaJogo.botaoGarrafa.setEnabled(false);
        }

        if (qtdVitamina > 0) {
            telaJogo.botaoVitamina.setEnabled(true);
        } else {
            telaJogo.botaoVitamina.setEnabled(false);
        }

        telaJogo.usuario = textoUsuario.getText();
        telaJogo.apelido = textoApelido.getText();
        telaJogo.idJogador = jogador.getIdJogador();

        if (textoApelido.getText().equals("Convidado")) {
            telaJogo.heroiSelecionado = "APRENDIZ";
        } else {
            telaJogo.heroiSelecionado = jogador.getHeroiSelecionado();
        }

        telaJogo.setVisible(true);

        this.dispose();
    }//GEN-LAST:event_botaoJogarActionPerformed

